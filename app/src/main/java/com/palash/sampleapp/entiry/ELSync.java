package com.palash.sampleapp.entiry;

public class ELSync {

    private String DateTime;
    private String CurrentSync;
    private String IsSyncing;

    public String getDateTime() {
        return DateTime;
    }

    public void setDateTime(String dateTime) {
        DateTime = dateTime;
    }

    public String getCurrentSync() {
        return CurrentSync;
    }

    public void setCurrentSync(String currentSync) {
        CurrentSync = currentSync;
    }

    public String getIsSyncing() {
        return IsSyncing;
    }

    public void setIsSyncing(String isSyncing) {
        IsSyncing = isSyncing;
    }
}
